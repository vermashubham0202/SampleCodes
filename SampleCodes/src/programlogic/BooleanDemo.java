package programlogic;
/*
12-Program to negate and print boolean variable.
 */

/**
 *
 * @author Shubham Verma
 */
public class BooleanDemo {

    public static void main(String args[]) {

        boolean flag = true;
        System.out.println(flag);

        //Negating boolean variable
        flag = !flag;
        System.out.println(flag);
    }
}
