package programlogic;
/*
19-Program to demonstrate switch case in java that supports string.
 */

/**
 *
 * @author Shubham Verma
 */
public class SwitchStringDemo {

    public static void main(String args[]) {

        String s = "Hi";

        switch (s) {
            case "Hi":
                System.out.println("Hi Akki!");
                break;

            case "Hello":
                System.out.println("Hello Akki!");
                break;
        }
    }
}
